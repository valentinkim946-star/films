let productsContainer = document.querySelector('.films')
let input = document.querySelector('input')
let modalWindow = document.querySelector('.big_haver')
let modalProduct = document.querySelector('.text6')

function render(data) {
  data.forEach(product => {
    productsContainer.insertAdjacentHTML('afterbegin',
      `
       <div>
            <img id='${product.id}' src="${product.image}" alt="${product.title}" onclick='modalOpen(event)'>
            <div>
                <h3>${product.title}</h3>
                <p>${product.price}</p>
            </div>
        </div>
      `)
  });
}

render(products)

input.addEventListener('input', () => {
  let filteredProducts = products.filter(prod => prod.title.toLowerCase().includes(input.value.toLowerCase()))
  productsContainer.textContent = ''
  render(filteredProducts)
})

function modalOpen(event) {
  modalWindow.style.display = 'flex'
  let findProduct = products.find(el => el.id == event.target.id)
  modalProduct.textContent = ''
  modalProduct.insertAdjacentHTML('afterbegin', 
    `
    <div>
        <span class='span-clos' onclick="modalClose()">Close</span>
    <img src="${findProduct.image}" alt="${findProduct.title}">
    <div class='text222'>
        <h3>${findProduct.title}</h3>
        <p>${findProduct.price}</p>
    </div>
    </div>
    `
  )
}

function modalClose() {
  modalWindow.style.display = 'none'
}